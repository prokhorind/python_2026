# Приклад 4: Вибір правильної структури даних

print("=== ВИБІР СТРУКТУРИ ДАНИХ ===")

# 1. СПИСОК - коли потрібен порядок та можливість змін
print("1. СПИСОК для списку покупок:")
shopping_list = ["хліб", "молоко", "яйця", "масло"]
print(f"Початковий список: {shopping_list}")

# Можемо додавати, видаляти, змінювати порядок
shopping_list.append("сир")
shopping_list.insert(1, "кефір")
print(f"Після змін: {shopping_list}")

# 2. СЛОВНИК - для швидкого доступу за ключем
print("\n2. СЛОВНИК для телефонної книги:")
contacts = {
    "Мама": "+380501234567",
    "Тато": "+380507654321", 
    "Друг Олексій": "+380631112233",
    "Школа": "+380442223344"
}

# Швидкий пошук за ім'ям
name = "Мама"
if name in contacts:
    print(f"Телефон {name}: {contacts[name]}")

# Додавання нового контакту
contacts["Бабуся"] = "+380509998877"
print(f"Всі контакти: {len(contacts)} записів")

# 3. КОРТЕЖ - для незмінних даних
print("\n3. КОРТЕЖ для дати народження:")
birth_date = (15, 8, 2007)  # день, місяць, рік
day, month, year = birth_date

print(f"Дата народження: {day}.{month:02d}.{year}")
print("Кортеж не можна змінити - це гарантує цілісність даних")

# 4. МНОЖИНА - для унікальних елементів
print("\n4. МНОЖИНА для відвіданих міст:")
visited_cities = {"Київ", "Львів", "Одеса", "Харків"}
print(f"Відвідані міста: {visited_cities}")

# Додаємо нове місто
visited_cities.add("Дніпро")
# Спробуємо додати місто, яке вже є
visited_cities.add("Київ")  # Не додасться, бо вже є

print(f"Після подорожей: {visited_cities}")
print(f"Всього відвідано міст: {len(visited_cities)}")

# ПРАКТИЧНИЙ ПРИКЛАД: Система оцінок студента
print("\n=== ПРАКТИЧНИЙ ПРИКЛАД ===")

# Інформація про студента (незмінна) - КОРТЕЖ
student_info = ("Марія", "Петренко", "10-Б")

# Оцінки за семестр (порядок важливий) - СПИСОК  
semester_grades = [10, 8, 9, 11, 7, 10, 9]

# Контакти (швидкий доступ за ключем) - СЛОВНИК
student_contacts = {
    "email": "maria.petrenko@school.edu.ua",
    "phone": "+380501234567",
    "parent_phone": "+380507654321"
}

# Предмети, які здав (унікальні) - МНОЖИНА
passed_subjects = {"математика", "фізика", "хімія", "біологія", "англійська"}

# Виведення інформації
name, surname, class_name = student_info
print(f"Студент: {name} {surname}, клас {class_name}")
print(f"Середня оцінка: {sum(semester_grades) / len(semester_grades):.1f}")
print(f"Email: {student_contacts['email']}")
print(f"Здано предметів: {len(passed_subjects)}")

# Додавання нової оцінки
semester_grades.append(12)
print(f"Після контрольної: середня оцінка {sum(semester_grades) / len(semester_grades):.1f}")

# Здача нового предмету
passed_subjects.add("інформатика")
print(f"Всього здано предметів: {len(passed_subjects)}")

print("\n=== ВИСНОВКИ ===")
