"""
📌 ТЕМА: Декоратори в Python на практиці

🎯 Мета:
Навчитися змінювати поведінку вже існуючих функцій БЕЗ зміни їхнього коду.

🧠 Ідея:
У нас є стандартна функція print().
Ми не можемо змінити її код (вона вбудована в Python),
але ми хочемо:
    - додати час виклику
    - логувати виклики у файл
    - бачити, коли саме щось було виведено

💡 Рішення:
Ми "обгорнемо" функцію print у власну функцію (декоратор).

📚 У цьому файлі:
1. Спочатку — обгортка вручну (без @)
2. Потім — той самий підхід через @декоратор
"""

import time


# =========================
# 🔹 ДЕКОРАТОР
# =========================

def log_calls(func):
    def wrapper(*args, **kwargs):
        current_time = time.strftime("%H:%M:%S")

        log_message = f"[LOG {current_time}] Виклик функції {func.__name__} з аргументами {args}"

        # лог у консоль
        print(log_message)

        # лог у файл
        with open("log.txt", "a", encoding="utf-8") as file:
            file.write(log_message + "\n")

        return func(*args, **kwargs)
    return wrapper


# =========================
# 🔹 1. ОБГОРТКА ВРУЧНУ
# =========================

# обгортаємо стандартний print
print_with_log = log_calls(print)

print_with_log("Привіт!")
print_with_log("Це вже логований print")


# =========================
# 🔹 2. ЧЕРЕЗ ДЕКОРАТОР @
# =========================

@log_calls
def my_print(text):
    print(text)


my_print("Привіт через декоратор!")
my_print("Ще один виклик")

print("звичайний виклик")
