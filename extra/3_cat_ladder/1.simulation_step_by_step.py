"""

Ідея:
Ми моделюємо рух драбини крок за кроком (дискретна симуляція).

Математика:
1. Драбина має фіксовану довжину:
   x² + y² = L²

2. Звідси:
   y = sqrt(L² - x²)

3. Кіт знаходиться на відрізку драбини:
   cat_x = x * (1 - k)
   cat_y = y * k

де:
- x — положення основи
- y — висота
- k — частка положення кота (0..1)
"""

import math

L = 10            # довжина драбини
steps = 50        # кількість кроків симуляції
k = 0.5           # кіт посередині драбини

trajectory = []

for i in range(steps + 1):
    # 1. Рухаємо основу драбини
    x = L * (i / steps)   # x змінюється від 0 до L

    # 2. Обчислюємо висоту через обмеження (теорема Піфагора)
    # x² + y² = L² → y = sqrt(L² - x²)
    y = math.sqrt(L**2 - x**2)

    # 3. Обчислюємо позицію кота через інтерполяцію по відрізку
    # Кіт = точка між A(x,0) і B(0,y)
    # Формула: C = A + k*(B - A)
    cat_x = x * (1 - k)
    cat_y = y * k

    trajectory.append((cat_x, cat_y))

print("Траєкторія кота:")
for point in trajectory:
    print(point)