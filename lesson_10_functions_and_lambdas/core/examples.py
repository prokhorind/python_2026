# =========================================
# ОДНА І ТА САМА ЗАДАЧА:
# помножити два числа
# =========================================


# =========================================
# 1. ПРОЦЕДУРНИЙ СТИЛЬ
# =========================================

a = 2
b = 3

result = a * b

print("Procedural:", result)


# =========================================
# 2. ЗВИЧАЙНА ФУНКЦІЯ (процедурний + абстракція)
# =========================================

def multiply(a, b):
    return a * b

print("Function:", multiply(2, 3))


# =========================================
# 3. LAMBDA (функціональний стиль)
# =========================================

multiply_lambda = lambda a, b: a * b

print("Lambda:", multiply_lambda(2, 3))


# =========================================
# 4. LAMBDA (без змінної)
# =========================================

print("Lambda:", (lambda a, b: a * b)(2, 3))


# =========================================
# 5. ООП
# =========================================

class Calculator:
    def multiply(self, a, b):
        return a * b

calc = Calculator()
print("OOP:", calc.multiply(2, 3))