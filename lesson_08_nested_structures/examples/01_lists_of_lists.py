# –ë–µ–∑–ø–µ—á–Ω–∏–π –¥–æ—Å—Ç—É–ø –¥–æ –µ–ª–µ–º–µ–Ω—Ç—ñ–≤
def safe_get(jagged_list, row, col):
    """–ë–µ–∑–ø–µ—á–Ω–æ –æ—Ç—Ä–∏–º—É—î –µ–ª–µ–º–µ–Ω—Ç –∑ –Ω–µ—Ä—ñ–≤–Ω–æ–º—ñ—Ä–Ω–æ–≥–æ —Å–ø–∏—Å–∫—É"""
