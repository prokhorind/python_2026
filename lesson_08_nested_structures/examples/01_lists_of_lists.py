# –ü–æ—à—É–∫ –µ–ª–µ–º–µ–Ω—Ç–∞ –≤ –º–∞—Ç—Ä–∏—Ü—ñ
def find_in_matrix(matrix, target):
    """–ó–Ω–∞—Ö–æ–¥–∏—Ç—å –ø–æ–∑–∏—Ü—ñ—é –µ–ª–µ–º–µ–Ω—Ç–∞ –≤ –º–∞—Ç—Ä–∏—Ü—ñ"""
    for i in range(len(matrix)):
        for j in range(len(matrix[i])):
            if matrix[i][j] == target:
                return (i, j)
    return None

