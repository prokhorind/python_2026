    """–û–±—á–∏—Å–ª—é—î —Å—É–º—É –≤—Å—ñ—Ö –µ–ª–µ–º–µ–Ω—Ç—ñ–≤ –º–∞—Ç—Ä–∏—Ü—ñ"""
