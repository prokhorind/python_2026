def transpose_matrix(matrix):
    """–¢—Ä–∞–Ω—Å–ø–æ–Ω—É—î –º–∞—Ç—Ä–∏—Ü—é (–º—ñ–Ω—è—î —Ä—è–¥–∫–∏ —Ç–∞ —Å—Ç–æ–≤–ø—Ü—ñ –º—ñ—Å—Ü—è–º–∏)"""
    rows = len(matrix)
    cols = len(matrix[0])
    
    transposed = []
    for j in range(cols):
        new_row = []
        for i in range(rows):
            new_row.append(matrix[i][j])
        transposed.append(new_row)
    
    return transposed

